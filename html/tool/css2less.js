function convent(newVal) {

    let val = newVal.replace(/\s+/g, " ")
        .replace(/[\t\n]|^\s*|\s$/g, "")
        .replace(/\s*\{\s*/g, " { \n")
        .replace(/\s*;\s*/g, ";\n")
        .replace(/\s*}\s*/g, "\n}\n")
        //.replace(/\/\*/g, "/*\n")
        .replace(/\*\//g, "*/\n");
    //.replace(/\n\*\//g, "\*\/\n")
    // .replace(/\*\//g, "\*\/\n");

    //   console.log(val.replace(/\/\*.*(\n).*\*\\/, ''));

    //   console.log(val)

    let list = val.split(/\n/);
    //  console.log(list)

    // 错误list
    let noList = [];
    //重复list
    let repList = [];
    //@ list
    let t_list = [];


    let map = {};

    function getBeginEnd(list, str) {
        let begin = str.match(/\{/g);
        if (begin) {
            list[0] += begin.length;
        }
        let end = str.match(/\}/g);
        if (end) {
            list[1] += end.length;
        }
        return list;
    }

    let beList = [0, 0];
    let beginMode = false;

    //注释 对象
    let notesList = [];
    let isNotes = false;
    list.forEach(d => {
        //是否为注释
        let e_idx = $.trim(d).indexOf("/*");
        if (e_idx >= 0) {
            isNotes = true;
        }
        if (isNotes) {
            notesList.push(d);
            if ($.trim(d).indexOf("*/") >= 0) {
                isNotes = false;
            }
        } else {
            //排除 @ -- 动画、media
            let d_idx = $.trim(d).indexOf("@");

            if (d_idx === 0) {
                beginMode = true;
                beList = [0, 0];
            }

            if (beginMode) {
                getBeginEnd(beList, d);
                t_list.push(d);
                if (beList[0] === beList[1]) {
                    beginMode = false;
                }
            } else {
                let idx = d.indexOf("{");
                if (idx >= 0) {
                    let name = d.substring(0, idx);
                    let val = d.substring(idx + 1, d.length - 1);
                    let _notesList = notesList;
                    notesList = [];
                    addPath($.trim(name), $.trim(val), _notesList);
                } else {
                    noList.push(d);
                }
            }

        }


    })

    // console.log(noList)

    function splitName(name) {

        name = name.replace(", ", ",");
        let arr = [name];
        let types = [" + ", " > ", " "];
        let typeList = [];
        let temList = [];
        types.forEach(d => {
            let idx = name.indexOf(d);
            if (idx >= 0) {
                typeList.push(d);
            }
        });
        typeList.forEach(d => {
            arr.forEach(p => {
                let tList = p.split(d).map((m, i) => {
                    if (i) {
                        return d + m;
                    } else {
                        return m
                    }
                })

                temList.push(...tList);
            })
            arr = temList;
            temList = [];
        });

        return arr;
    }

    function addPath(name, valStr, _notesList) {
        let val = $.trim(valStr);
        if (val) {
            let end = val[val.length - 1];
            if (end !== ";") {
                val += ";";
            }
        }

        let list = splitName(name);
        let item = null;
        list.forEach((d, i) => {
            if (!i) {
                if (!map[d]) {
                    map[d] = {
                        val: ""
                    };
                }
                item = map[d];
            } else {
                if (!item[d]) {
                    item[d] = {
                        val: ""
                    };
                } else {
                    repList.push({
                        name: d,
                        text: item[d],
                        val: val
                    });
                }
                item = item[d];
            }

        })
        item.notes = _notesList;
        item.val += val;

    }


    function outVal(map, h) {
        let fn = function (item, h) {
            if (item.notes && item.notes.length) {
                h.push(item.notes.join(""))
            }
            h.push(item.val);
            let flag = false;
            for (let key in item) {
                if (key === "val" || key === "notes") {
                    continue;
                }
                if (item.hasOwnProperty(key)) {
                    flag = true;
                    h.push("\n");
                    h.push(key);
                    h.push("{");
                    fn(item[key], h);
                    h.push("}");
                }
            }
            if (flag) {
                h.push("\n");
            }
        }

        for (let key in map) {
            if (map.hasOwnProperty(key)) {
                h.push(key);
                h.push("{");
                fn(map[key], h);
                h.push("}\n");
            }
        }
    }

    let h = [];
    outVal(map, h)

    t_list.forEach(d => {
        h.push(d);
    })


    return h.join(" ");
    //  console.log(h.join(" "))


}

